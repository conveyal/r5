    @ParameterizedTest(name = "road {0} - {1} - {2}")
    @MethodSource("data")
    public void testRoadAngles(int startAngle, int middleAngle, int endAngle, String wktlineString)
            throws ParseException {
        WKTReader wktReader = new WKTReader();

        startAngle = startAngle == -180 ? 180 : startAngle;
        endAngle = endAngle == -180 ? 180 : endAngle;
        LineString lineString = (LineString) wktReader.read(wktlineString);
        LineString reverseLine = (LineString) lineString.reverse();

        // ==== First angle
        assertEquals(startAngle, Math.toDegrees(DirectionUtils.getFirstAngle(lineString)), delta);

        // ==== Last Angle
        assertEquals(endAngle, Math.toDegrees(DirectionUtils.getLastAngle(lineString)), delta);

        // ==== Reversed angle
        // Tests if we can get values of first and last angles for backward edge from forward edge
        // angles

        // How are first and last angle if this is forward edge
        double radAngleFirst = DirectionUtils.getFirstAngle(lineString);
        double radAngleLast = DirectionUtils.getLastAngle(lineString);

        final byte bradForwardFirstAngle = radiansToBrads(radAngleFirst);
        final byte bradForwardLastAngle = radiansToBrads(radAngleLast);

        // How are first and last angles if this is backward edge
        double radRAngleFirst = DirectionUtils.getFirstAngle(reverseLine);
        double radRAngleLast = DirectionUtils.getLastAngle(reverseLine);

        final byte bradBackwardLastAngle = radiansToBrads(radRAngleLast);
        final byte bradBackwardFirstAngle = radiansToBrads(radRAngleFirst);

        // Double because we need to have delta since conversion from radians -> brads -> degrees is
        // lossy
        assertEquals(
                startAngle == 180 ? -180 : startAngle, bradsToDegree(bradForwardFirstAngle), 1);
        assertEquals(endAngle == 180 ? -180 : endAngle, bradsToDegree(bradForwardLastAngle), 1);

        // We need to calculate backward angles from forward angles
        // This can be done if we subtract 180 degrees

        // First forward angle is at the same place as last backward angle
        assertEquals(bradBackwardLastAngle, (byte) (bradForwardFirstAngle - m180));
        // Last forward angle is at the same place as first backward angle
        assertEquals(bradBackwardFirstAngle, (byte) (bradForwardLastAngle - m180));
    }
